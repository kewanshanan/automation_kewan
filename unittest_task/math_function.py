def modulo_func(number):
    result = number % 2
    return result


def is_even(number):
    return number % 2 == 0


def is_odd(number):
    return number % 2 != 0


def sum_function(num_a, num_b):
    return num_a + num_b


def sub_function(num_a, num_b):
    return num_a - num_b


def mul_function(num_a, num_b):
    return num_a * num_b


def div_function(num_a, num_b):
    return num_a / num_b
